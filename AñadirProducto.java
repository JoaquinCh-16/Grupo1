    private String generateProductoId() {
        String ultimoId = productoController.obtenerUltimoProductoId();
        int numero = Integer.parseInt(ultimoId.replace("PR", "")) + 1;
        return "PR" + String.format("%03d", numero);
    }
    
    
        
    

    
    
  
    
    
    
