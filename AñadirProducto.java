    private void btnGuardarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProActionPerformed
     try {
        String nombre = txtNomPro.getText().trim();
        String categoria = (String) cmbCategoPro.getSelectedItem();
        Double precio = txtPrecioPro.getText().isEmpty() ? null : Double.parseDouble(txtPrecioPro.getText().trim());
        Integer stock = txtStockPro.getText().isEmpty() ? null : Integer.parseInt(txtStockPro.getText().trim());

        System.out.println("Datos ingresados - Nombre: " + nombre + ", Categor鱈a: " + categoria + ", Precio: " + precio + ", Stock: " + stock);
        if (nombre == null || nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del producto no puede estar vac鱈o.");
            return;
        }
        if (categoria == null || categoria.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La categor鱈a no puede estar vac鱈a.");
            return;
        }
        if (precio == null || precio <= 0) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un valor positivo.");
            return;
        }
        if (stock == null || stock < 0) {
            JOptionPane.showMessageDialog(this, "El stock no puede ser negativo.");
            return;
        }
        // Llamada al controlador (sin generar ID aqu鱈)
        if (productoController.agregarProducto(generateProductoId(),nombre, estado, precio, stock, categoria)) {
            JOptionPane.showMessageDialog(this, "Producto a単adido exitosamente.");
            ProductoFrame ProductoFrame = new ProductoFrame();
            ProductoFrame.setVisible(true);
            ProductoFrame.pack();
            ProductoFrame.setLocationRelativeTo(null);
            this.dispose();
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al a単adir el producto.");
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Precio y stock deben ser valores num辿ricos v叩lidos.");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Ocurri坦 un error: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnGuardarProActionPerformed

 
    
