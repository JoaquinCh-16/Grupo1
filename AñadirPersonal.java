    private void btnAñadirPerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirPerActionPerformed
        try {
        String nombre = txtNomPer.getText().trim(); // Elimina espacios al inicio y final
        String email = txtEmailPer.getText().trim();
        String contrasena = txtContra.getText().trim();
        Integer telefono = txtTelePer.getText().isEmpty() ? null : Integer.parseInt(txtTelePer.getText().trim());
        String rol = (String) cmbRol.getSelectedItem();

        if (contrasena == null || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La contraseña no puede estar vacía.");
            return;
        }

        // Intentar agregar el personal
        if (personalController.agregarPersonal(generatePersonalId(), nombre, email, contrasena, telefono, rol)) {
            JOptionPane.showMessageDialog(this, "Personal añadido exitosamente.");
            
            // Redirigir a la ventana de la tabla (o lista de personal)
            personalFrame.actualizarTabla();  // Si necesitas actualizar la tabla

            // Mostrar la ventana de personal
            Personal PersonalFrame = new Personal();
            PersonalFrame.setVisible(true);
            PersonalFrame.pack();
            PersonalFrame.setLocationRelativeTo(null);

            // Cerrar la ventana actual
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al añadir personal (verifica la contraseña). La contraseña debe tener al menos 8 caracteres, una mayúscula, una minúscula, una letra y un carácter especial.");
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Teléfono debe ser un número válido.");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Ocurrió un error: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnAñadirPerActionPerformed

