    private String generatePersonalId() {
        String ultimoId = personalController.obtenerUltimoPersonalId(); // Añade este método en PersonalController
        int numero = Integer.parseInt(ultimoId.replace("P", "")) + 1;
        return "P" + String.format("%03d", numero);
    }
    
    
