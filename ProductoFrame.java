    private void btnEditarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProActionPerformed
        int selectedRow = tblProd.getSelectedRow(); // tu tabla de productos

    if (selectedRow >= 0) {
        String productoId = (String) tblProd.getValueAt(selectedRow, 0); // ID
        String nombre = (String) tblProd.getValueAt(selectedRow, 1);     // Producto
        double precio = Double.parseDouble(tblProd.getValueAt(selectedRow, 2).toString()); // Precio
        int stock = Integer.parseInt(tblProd.getValueAt(selectedRow, 3).toString());       // Stock
        String categoria = (String) tblProd.getValueAt(selectedRow, 4);  // Categoría
        String estado = (String) tblProd.getValueAt(selectedRow, 5);     // Estado

        EditarProducto editarFrame = new EditarProducto(productoId, nombre, estado, precio, stock, categoria);
        editarFrame.setVisible(true);
        editarFrame.pack();
        editarFrame.setLocationRelativeTo(null);
        this.dispose(); // cerrar el frame actual si lo deseas
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona un producto para editar.");
    }
    }//GEN-LAST:event_btnEditarProActionPerformed

    private void btnEliminarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProActionPerformed
//revisar
        int selectedRow = tblProd.getSelectedRow();

        if (selectedRow >= 0) {
            String productoId = (String) tblProd.getValueAt(selectedRow, 0); // ID en la primera columna
            try {
                if (pcontroller.eliminarProducto(productoId)) { // sin paréntesis extra
                    JOptionPane.showMessageDialog(this, "Producto eliminado exitosamente.");
                    cargarProductos(); // recarga la tabla
                } else {
                    JOptionPane.showMessageDialog(this, "Error al eliminar producto.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ProductoFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un producto para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarProActionPerformed

