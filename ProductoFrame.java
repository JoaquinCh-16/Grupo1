    public void cargarProductos() {
        try {
            ConexionBD conexion = new ConexionBD();
            Connection conn = conexion.getConnection();
            CallableStatement cs = conn.prepareCall("{call SP_LISTAR_PRODUCTOS}");
            ResultSet rs = cs.executeQuery();

            DefaultTableModel modelo = (DefaultTableModel) tblProd.getModel();
            modelo.setRowCount(0);

            while (rs.next()) {
                Object[] fila = new Object[6];
                fila[0] = rs.getString("PRODUCTO_ID");
                fila[1] = rs.getString("NOMBRE");
                fila[2] = rs.getDouble("PRECIO");
                fila[3] = rs.getInt("STOCK");
                fila[4] = rs.getString("CATEGORIA");
                fila[5] = rs.getString("ESTADO");

                modelo.addRow(fila);
            }

            rs.close();
            cs.close();
            conn.close();

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los productos: " + e.getMessage());
        }
    }

